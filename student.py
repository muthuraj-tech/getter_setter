class Student:

    def get_name(self): 
        return self._name 
      
    def set_name(self, name): 
        self._name = name 

    def get_age(self): 
        return self._age 
      
    def set_age(self, age): 
        self._age = age

    def get_addr(self): 
        return self._addr 
      
